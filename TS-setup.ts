// How to setup Typescript with your project:-

// 1. npm install --dev typescript (ideally it should be a dev dependency.) (You can also install it globally.)
// 2. npx tsc --init (makes tsconfig.json that tells the TS compiler about a lots of jargons.)
// 3. npx tsc -b (To convert TS code to JS before running your application.)
// 4. "npm run dist/index.js" or "npm run build/index.js" (for running your application.) 
